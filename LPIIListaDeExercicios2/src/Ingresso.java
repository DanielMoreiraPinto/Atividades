
public class Ingresso {

	private double valor;
	private int cadeira;
	
	public Ingresso(int cadeira) {	
		this.cadeira = cadeira;
	}
	
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getCadeira() {
		return cadeira;
	}
	public void setCadeira(int cadeira) {
		this.cadeira = cadeira;
	}
	
}
